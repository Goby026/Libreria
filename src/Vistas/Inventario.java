    public void cabecera(){
        String cabecera[] = {"CODIGO","NOMBRE","STOCK","FECHA INGRESO","PRECIO UNITARIO","MARCA"};
        model = new DefaultTableModel(null, cabecera);
        tbl_inventario.setModel(model);
        cargarInventario();
        txt_total_art.setText(""+tbl_inventario.getRowCount());
    }
    
    
    public void cargarInventario(){
        String sql = "SELECT `id`, `descripcion`, `stock_prod`, `fecha_ingreso_prod`, `precio`, `id_marca` FROM `producto`";
        String datos[] = new String[6];
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("id");
                datos[1] = rs.getString("descripcion");
                datos[2] = rs.getString("stock_prod");
                datos[3] = rs.getString("fecha_ingreso_prod");
                datos[4] = rs.getString("precio");
                datos[5] = rs.getString("id_marca");
                model.addRow(datos);
            }
            tbl_inventario.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    
    public void getDatosEmpresa(){
        String sql = "SELECT `nom_empresa`,`ciudad`, `telf_empresa` FROM `tconfiguracion` WHERE id_config = 1";
        String datos[] = new String[3];
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                txt_razon_social.setText(rs.getString("nom_empresa"));
                txt_ciudad.setText(rs.getString("ciudad"));
                txt_telefono.setText(rs.getString("telf_empresa"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

